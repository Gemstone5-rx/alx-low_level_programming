#include <stdio.h>

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 *  mul - multiplies two integers
 *  @a: first integer
 *  @b: second integer
 *
 *  Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - gives the remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
